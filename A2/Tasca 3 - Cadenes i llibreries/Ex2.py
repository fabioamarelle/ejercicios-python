import random

# Genera tres nombres aleatoris entre 1 i 100
nAleatori1 = random.randint(1,100)
nAleatori2 = random.randint(1,100)
nAleatori3 = random.randint(1,100)

# Suma els tres nombres
nAleatoriSuma = nAleatori1 + nAleatori2 + nAleatori3

print(f"El primer nombre aleatori és {nAleatori1}.")
print(f"El segon nombre aleatori és {nAleatori2}.")
print(f"El tercer nombre aleatori és {nAleatori3}.")
print(f"La suma d'aquests nombres és {nAleatoriSuma}.")
