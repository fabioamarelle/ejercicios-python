# Demana els nombres necessaris
a = float(input("Introdueix el primer nombre: "))
b = float(input("Introdueix el segon nombre: "))

# Calcula tots els resultats utilitzant les variables
suma = a + b
resta = a - b
multiplicacio = a * b
divisio = a / b

# Mostra tots els resultats per pantalla
print(f"{a} + {b} = {suma}")
print(f"{a} - {b} = {resta}")
print(f"{a} * {b} = {multiplicacio}")
print(f"{a} / {b} = {divisio}")